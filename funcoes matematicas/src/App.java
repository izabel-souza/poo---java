public class App {
    public static void main(String[] args) {
        
        double raiz = Math.sqrt(9);
        System.out.printf("Raiz quadrada de 9 = %.2f%n", raiz);

        double potencia = Math.pow(4, 4);
        System.out.printf("Potencia de 4^4 = %.2f%n", potencia);

        double x = -10.0;
        double absoluto = Math.abs(x);
        System.out.printf("O valor absoluto de -10 = %2.f%n", absoluto);

    }
}
